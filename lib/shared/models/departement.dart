class Departement {
  const Departement({
    required this.id,
    required this.name,
    required this.manager,
    required this.headcount,
    required this.budget,
    required this.pole,
    required this.size,
    required this.location,
  });

  final String id;
  final String name;
  final String manager;
  final int headcount;
  final String budget;
  final String pole;
  final String size;
  final String location;
}
